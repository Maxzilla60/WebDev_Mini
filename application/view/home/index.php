	<p>MINI is an extremely simple and easy to understand skeleton PHP application, reduced to the max.
	MINI is NOT a professional framework and it does not come with all the stuff real frameworks have.
	If you just want to show some pages, do a few database calls and a little-bit of AJAX here and there, without
	reading in massive documentations of highly complex professional frameworks, then MINI might be very useful for you.
	MINI is easy to install, runs nearly everywhere and doesn't make things more complicated than necessary.</p>
	<p>For a deeper introduction into MINI have a look into this blog post:
	<a href="http://www.dev-metal.com/mini-extremely-simple-barebone-php-application/">MINI, an extremely simple barebone PHP application</a>.</p>
	<h2>Features</h2>
	<ul>
	<li>extremely simple, easy to understand</li>
	<li>simple but clean structure</li>
	<li>makes "beautiful" clean URLs</li>
	<li>demo CRUD actions: Create, Read, Update and Delete database entries easily</li>
	<li>demo AJAX call</li>
	<li>tries to follow PSR 1/2 coding guidelines</li>
	<li>uses PDO for any database requests, comes with an additional PDO debug tool to emulate your SQL statements</li>
	<li>commented code</li>
	<li>uses only native PHP code, so people don't have to learn a framework</li>
	</ul>
